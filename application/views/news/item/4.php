    <div class="post-body">
        <p>2017年3月4日下午，成都宏明电子股份有限公司对阵四川九牛足球联谊赛于成都谢菲联足球俱乐部拉开战幕，成都宏明电子股份有限公司董事长何刚到场观看比赛。</p>
		<p>本场比赛由宏明电子客场挑战九牛。比赛开始三分钟，九牛利用对手右路的防守空当，获得本场第一个角球。一开场的硝烟弥漫为本场比赛的节奏奠定了基础。比赛到九分钟，九牛的边路发起进攻，右路传中至禁区，边前卫薛飞抓住机会，禁区前抽射得分，获得本场比赛的第一个进球。这个进球激起了宏明电子的斗志，随后他们利用后场对方的失误抢断，由右边路发起进攻，行至对方禁区，由2号吴宏抓住机会头球攻门。无奈角度太过刁钻，球高于球门飞出。这次的进攻对九牛的禁区造成了不小的威胁。随后的比赛中，宏明电子获得一个位置极佳的前场定位球，但九牛的门将发挥神勇，将球扑出。</p>
		<p>比赛中双方队员各有精彩的表现，尤其是宏明电子的球员在面对强大的对手展现出的不怕困难、敢于拼搏的精神感染了现场所有人。本次比赛不仅有利于企业员工的身心健康，更提供了一个相互交流的平台，让宏明人能够在比赛中淬炼出团结拼搏的勇气和毅力，这种精神也有利于企业的发展。九牛队对于宏明电子在比赛中的表现给予了高度的赞扬，最终足球联谊赛在掌声中落下帷幕。(纪欢）</p>
